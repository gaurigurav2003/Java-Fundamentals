package if_else_assignment_0;

public class Ifelse_demo2 {

	public static void main(String[] args) {
		// write a java program, take a number , and print whether it is a less than 10 or greater than 10
		int var=10;
		if(var<10) {
			System.out.println(var + " is a less than 10");
		
		}else if(var>10) {
			System.out.println(var + " is greater than 10");
		
		}else {
			System.out.println("it is equal to 10");
		}

	}
}


